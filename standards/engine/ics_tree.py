def get_children(code: str) -> list:
    """获取某节点的直接子节点"""
    return sorted(
        [c for c, info in ICS_TREE.items() if info["parent"] == code],
        key=lambda x: x
    )


def get_parent_chain(code: str) -> list:
    """获取从根到节点的完整路径"""
    chain = []
    while code:
        chain.insert(0, code)
        info = ICS_TREE.get(code, {})
        code = info.get("parent", "")
    return chain


def get_display_path(code: str, sep: str = " / ") -> str:
    """获取可读路径：'电气工程/照明/灯具'"""
    chain = get_parent_chain(code)
    names = [ICS_TREE.get(c, {}).get("name", c) for c in chain]
    return sep.join(names)


def get_root_codes() -> list:
    """获取所有根节点"""
    return sorted([c for c, info in ICS_TREE.items() if info["level"] == 1])
