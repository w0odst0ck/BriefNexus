def normalize_std_no(std_no: str) -> str:
    """标准化标准号：提取数字前缀部分

    GB/T 7000.1-2023 → 7000.1
    GB/T 30104.103-2026 → 30104.103
    GB/Z 187-2026 → 187
    """
    no = std_no.strip().upper()
    # 去掉 GB/GB/T/GB/Z 等前缀
    no = re.sub(r'^GB\s*/?\s*[A-Z]*\s*', '', no)
    # 去掉年份
    no = re.sub(r'-\d{4}$', '', no)
    # 去空格
    no = no.replace(' ', '')
    return no


def resolve_intl_source(standard_no: str, title: str = '') -> Optional[dict]:
    """根据 GB/T 标准号和标题解析对应的国际标准

    Returns:
        {
            'intl_source': 'IEC 60598-1',
            'intl_description': '灯具 第1部分：一般要求与试验',
            'type': 'iec' | 'iso' | 'cie' | 'cispr',
        }
        或 None（无法识别）
    """
    no = normalize_std_no(standard_no)

    # 1. 精确匹配
    if no in EXACT_MAP:
        intl_code, desc = EXACT_MAP[no]
        return _make_result(intl_code, desc)

    # 2. 前缀匹配（从长到短）
    sorted_keys = sorted(EXACT_MAP.keys(), key=len, reverse=True)
    for key in sorted_keys:
        if no.startswith(key + '.') or no == key:
            intl_code, desc = EXACT_MAP[key]
            return _make_result(intl_code, desc)

    # 3. 再试：只取整数前缀
    int_prefix = re.match(r'^(\d+)', no)
    if int_prefix:
        p = int_prefix.group(1)
        for key in sorted_keys:
            if key.startswith(p) or p.startswith(key):
                if no.startswith(key.split('.')[0]):
                    intl_code, desc = EXACT_MAP[key]
                    return _make_result(intl_code, desc)

    return None


def _make_result(intl_code: str, description: str) -> dict:
    """构造标准化返回"""
    intl_code = intl_code.strip()
    # 判断类型
    prefix = intl_code.split()[0] if ' ' in intl_code else intl_code
    src_type = 'iec'
    if prefix.upper().startswith('ISO'):
        src_type = 'iso'
    elif prefix.upper().startswith('CIE'):
        src_type = 'cie'
    elif prefix.upper().startswith('CISPR'):
        src_type = 'cispr'

    return {
        'intl_source': intl_code,
        'intl_description': description.strip(),
        'type': src_type,
    }


# ── 批量分类 ──────────────────────────────────────────

def classify_standards(standards: list) -> list:
    """批量分类标准并标记是否采标

    规则：
      - 能匹配到 EXACT_MAP → 采标, 记录 intl_source
      - 不能匹配到 EXACT_MAP → 非采标(国内自主制定)
    """
    for s in standards:
        no = s.get('standard_no', '')
        title = s.get('title', '')

        result = resolve_intl_source(no, title)
        if result:
            s['is_adopted'] = True
            s['intl_source'] = result['intl_source']
            s['intl_type'] = result['type']
            s['intl_desc'] = result['intl_description']
        else:
            s['is_adopted'] = False
            s['intl_source'] = ''
            s['intl_type'] = ''
            s['intl_desc'] = ''

    return standards


def main():
    """CLI: 分类所有标准并更新数据库"""
    from standards.engine.storage import StandardDB
    db = StandardDB()
    rows = db._conn_execute(
        'SELECT id, standard_no, title FROM standards ORDER BY publish_date ASC'
    ).fetchall()

    items = [dict(r) for r in rows]
    classified = classify_standards(items)

    adopted_count = 0
    domestic_count = 0
    unknown_count = 0

    for item in classified:
        is_adopted = 1 if item.get('is_adopted') else 0
        intl_source = item.get('intl_source', '')

        db._conn_execute(
            'UPDATE standards SET is_adopted = ?, intl_source = ? WHERE id = ?',
            (is_adopted, intl_source, item['id'])
        )

        if is_adopted:
            adopted_count += 1
        elif intl_source:
            domestic_count += 1
        else:
            unknown_count += 1

    db._connect().commit()

    # 统计
    print(f'总条数:      {len(classified)}')
    print(f'采标(有IEC源): {adopted_count}')
    print(f'非采标(国内):  {domestic_count}')
    print(f'未知(待手动):  {unknown_count}')
    print()

    # 按国际标准源统计
    src_counter = Counter()
    for item in classified:
        if item.get('intl_source'):
            prefix = item['intl_source'].split()[0] if ' ' in item['intl_source'] else item['intl_source']
            src_counter[prefix] += 1

    print('按国际来源组织:')
    for src, cnt in src_counter.most_common():
        print(f'  {src:15s} {cnt} 条')

    db.close()
    print()
    print('✅ 数据库已更新（is_adopted + intl_source）')


if __name__ == '__main__':
    main()
